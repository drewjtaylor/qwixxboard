const totalMarks = 0;

/**
 * Takes in the parameter and increases it by
 * @param {Integer} totalMarks 
 * @returns totalMarks + 1
 */
export const increaseMarks = (totalMarks) => (totalMarks++);

/**
 * 
 * @param {Integer} totalMarks 
 * @returns 
 */
export const decreaseMarks = (totalMarks) => (totalMarks--);
